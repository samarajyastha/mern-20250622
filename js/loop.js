// Loop: Repeatedly do some tasks
// For loop
/**
 * for (start point; condition; increment/decrement ) {
 *
 * }
 *
 */

/**
 * i=1 => print 1
 * i=2 => print 2
 * i=3 => print 3
 * i=4 => print 4
 * i=5 => print 5
 * i=6
 */
for (let i = 1; i <= 5; i++) {
  // code
  console.log(i);
}

for (let i = 5; i >= 1; i--) {
  // code
  console.log(i);
}

// While loop
/**
 * start point
 *
 * while (condition){
 *
 *
 * incr/dcr
 * }
 */

let i = 1;

/**
 * i=1 => print 1
 * i=2 => print 2
 * i=3 => print 3
 * i=4 => print 4
 * i=5 => print 5
 * i=6
 */
while (i <= 5) {
  // code
  console.log(i);

  i++;
}

